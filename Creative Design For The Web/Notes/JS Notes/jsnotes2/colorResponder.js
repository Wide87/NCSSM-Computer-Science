//converts the input to lowercase
let userInput = prompt("What's your favorite color?").toLowerCase();
//checks against a list of colors
if (userInput === "blue") {console.log("That's mine too!");}
else if (userInput === "red") {console.log("A strong choice, like fire!");}
else if (userInput === "green") {console.log("Green is such a wonderful color!");}
else {console.log("That's a cool color!");}